    private List<String> loadUserData(String filePath){
        List<String> data = null;
        try{
          BufferedReader reader = new BufferedReader(new FileReader(filePath));  
          String line = reader.readLine();
          data = Arrays.asList(line.split(","));
        }
        catch(IOException e){
            
        
        }
        return data;
    }

